def add(a:float, b:float) -> float:
    """
    Adiciona dois nÃºmeros e retorna o resultado.

    Examples:
        >>> add(2, 3)
        5

        >>> add(-1, 1)
        0

        >>> add(0, 0)
        0

    Args:
        a (float): Numero  a ser somado.
        b (float): Numero  a ser somado.

    Returns:
        float : Resultado da soma a+b.
    """
    return a + b
